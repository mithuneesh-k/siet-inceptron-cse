async function seed() {
  const pass = bcrypt.hashSync('password123', 10);
  let success = 0, failed = 0;

  for (let i = 0; i < students.length; i++) {
    const [name, regNo, email, cls, batch] = students[i];
    const rollNo = `24CS${String(i + 1).padStart(3, '0')}`;
    const cleanEmail = email.replace(/\s+/g, '').toLowerCase();

    // Insert into users table
    const { data: newUser, error: userErr } = await supabase
      .from('users')
      .upsert({ email: cleanEmail, password_hash: pass, role: 'student' }, { onConflict: 'email' })
      .select('id')
      .single();

    if (userErr || !newUser) {
      console.error(`❌ [${rollNo}] Failed to upsert user for ${name}: ${userErr?.message}`);
      failed++;
      continue;
    }

    // Insert into students table
    const { error: profileErr } = await supabase.from('students').upsert({
      user_id: newUser.id,
      name: name.trim().toUpperCase(),
      roll_no: rollNo,
      reg_no: regNo,
      year: 2,
      class: cls,
      batch: batch,
    }, { onConflict: 'user_id' });

    if (profileErr) {
      console.error(`❌ [${rollNo}] Failed to upsert student profile for ${name}: ${profileErr?.message}`);
      failed++;
    } else {
      console.log(`✅ [${rollNo}] ${name} (${cls})`);
      success++;
    }
  }

  console.log(`\n🎓 Done! ${success} seeded, ${failed} failed.`);
}

seed().catch(console.error);
